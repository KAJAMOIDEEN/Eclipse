package basics;

//access specifier or modifier
public class Datatypes_Variables {
	public static void main(String[] args) {

		// Garbage Collector

		// primitive data types
		byte b = 10; /// -128 + 127
		short s = 200;
		int i = 3000;
		long l = 40000000000000000l;
		float f = 9098.9f;
		double d = 444444.900999;
		boolean bool = true;
		char initial = 'K';

		// non-primitive data types
		String name = "Java Program";

		System.out.print("Byte:" + b + "\nShort:" + s + "\nInteger:" + i + "\nLong:" + l + "\nFloat:" + f + "\nDouble:"
				+ d + "\nBoolean:" + bool + "\nChar:" + initial + "\nString:" + name);

	}

}
